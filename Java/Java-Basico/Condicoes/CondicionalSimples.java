package Condicoes;

public class CondicionalSimples {
    public static void main(String[] args) {

        //Variavel
        int idade = 18;


        //Estrutura de Condição Simples
        if (idade >= 18) {
            System.out.println("Você é de maior!");
        }
        else {
            System.out.println("Você ainda é de Menor!");
        }
    }
}
