package Operadores;

public class OperadoresUnitario {
    public static void main(String[] args) {
        //Valores Unitario

        int numero = 23;

        // "-" Transformar o Valor em Negativo
        System.out.println(- numero);

        // "+" Transformar o Valor em Positivo
        System.out.println(+ numero);

        // "++" Incrementa o Valor
        numero ++;
        System.out.println(numero);


        // "--" Descrementa o Valor
        numero --;
        System.out.println(numero);
    }

}
